package implementasi;


public class ArrayForLoop {
    public static void main(String[] args) {
        // Inisialisasi array
        int[] arr = {10, 20, 30, 40, 50};

        for (int i = 0; i < arr.length; i++) {
            System.out.println("Index: " + i + ", Value: " + arr[i]);
        }
    }
}


